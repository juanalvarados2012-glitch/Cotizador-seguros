// Idioma inicial: preferencia guardada → si no, idioma del navegador
// (navegadores en inglés, típicos en USA, arrancan en inglés).
export function detectLang() {
  try {
    const saved = localStorage.getItem("cotizador_lang");
    if (saved === "es" || saved === "en") return saved;
  } catch { /* sin localStorage */ }
  try {
    const nav = (navigator.language || "es").toLowerCase();
    return nav.startsWith("es") ? "es" : "en";
  } catch { return "es"; }
}

export function saveLang(lang) {
  try { localStorage.setItem("cotizador_lang", lang); } catch { /* noop */ }
}

